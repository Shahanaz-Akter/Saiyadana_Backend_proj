let renderProduct = "";
let bestofElctronic_product_itemEl = document.getElementById("card-product-container-1");

function renderProducts() {
  renderProduct = "";
  for (let i = 0; i < electronicsProductData.length; i++) {
    renderProduct += `
      <div>
        <a href="${electronicsProductData[i].link}" class="BestofElectronic_item">   
          <div class="bestOfElectornic_image_Product">
            <img src="${electronicsProductData[i].img}" />
          </div>
          <div class="bestofElectronicmoreOption">
            <p class="bestofElectornicProduct_name">${electronicsProductData[i].productName}</p>
            <p class="bestofElecronic_discount">${electronicsProductData[i].discount}</p>
            <p class="bestofElectronic_brand">${electronicsProductData[i].brand}</p>
          </div>
        </a>
      </div>`;
  }
  bestofElctronic_product_itemEl.innerHTML = renderProduct;
}

function handleResize() {
  const windowWidth = window.innerWidth;
  const itemWidth = 200; // Adjust this value based on your item width
  const containerWidth = bestofElctronic_product_itemEl.offsetWidth;
  const maxTranslation = containerWidth - windowWidth;

  const cardBtnRightEl = document.getElementById("card-btn-right-1");
  const cardBtnLeftEl = document.getElementById("card-btn-left-1");

  if (maxTranslation > 0) {
    cardBtnRightEl.style.display = "block";
    cardBtnLeftEl.style.display = "none";
  } else {
    cardBtnRightEl.style.display = "none";
    cardBtnLeftEl.style.display = "none";
    bestofElctronic_product_itemEl.style.transform = "translateX(0)";
    return;
  }

  cardBtnRightEl.addEventListener("click", function () {
    const translateX = getTranslateXValue(bestofElctronic_product_itemEl);
    const newTranslateX = Math.max(translateX - itemWidth, -maxTranslation);
    bestofElctronic_product_itemEl.style.transform = `translateX(${newTranslateX}px)`;
    if (newTranslateX === -maxTranslation) {
      cardBtnRightEl.style.display = "none";
      cardBtnLeftEl.style.display = "block";
    } else {
      cardBtnRightEl.style.display = "block";
      cardBtnLeftEl.style.display = "block";
    }
  });

  cardBtnLeftEl.addEventListener("click", function () {
    const translateX = getTranslateXValue(bestofElctronic_product_itemEl);
    const newTranslateX = Math.min(translateX + itemWidth, 0);
    bestofElctronic_product_itemEl.style.transform = `translateX(${newTranslateX}px)`;
    if (newTranslateX === 0) {
      cardBtnLeftEl.style.display = "none";
      cardBtnRightEl.style.display = "block";
    } else {
      cardBtnLeftEl.style.display = "block";
      cardBtnRightEl.style.display = "block";
    }
  });
}

function getTranslateXValue(element) {
  const style = window.getComputedStyle(element);
  const transformMatrix = new WebKitCSSMatrix(style.transform);
  return transformMatrix.m41;
}

window.addEventListener("resize", handleResize);
renderProducts();
handleResize();



